fn run() -> Result<(), String> {
    let mut cfg = Config::from_env_and_args()?;
    cfg.prompt_missing()?;

    let spec = Spec::from(&cfg);

    if cfg.dry_run {
        spec.print();
        return Ok(());
    }

    let runner = ShellRunner;
    spec.validate_host(&runner)?;
    spec.install(&runner)?;
    spec.print_done(&runner)?;

    Ok(())
}
